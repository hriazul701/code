#Tuple
items = ("Shrawan", "Janhavi", "banana", "Kiwi")
print(items)
#Accesing Element
print(items[0])
print(items[3])
print(items[-2])


#Nested Tuple
items2 =  ("Shrawan", "Janhavi", ('Riaz', 7),"banana", "Kiwi")
print(items2)
#REpetation
print(items2*3)


# Concatenation
print(items + items2)